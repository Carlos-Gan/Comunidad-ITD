import 'package:flutter/material.dart';

class Clase {
  final String materia;
  final String profesor;
  final String horario;
  final String hora;
  final String aula;
  final Color color;
  final IconData icon;

  Clase({
    required this.materia,
    required this.profesor,
    required this.horario,
    required this.hora,
    required this.aula,
    required this.color,
    required this.icon,
  });
}